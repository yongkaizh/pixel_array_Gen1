import { LayoutConfig } from "../../types";
import { SkillBuilder } from "./SkillBuilder";
import { findMaxActiveRow, generateMosaics } from "./mosaics";
import { generateCentering } from "./centering";

export function generateSkillCode(config: LayoutConfig): string {
  const builder = new SkillBuilder();
  
  builder.push("; ===================================================================");
  builder.push("; Cadence SKILL Pixel Array Layout Generation Script");
  builder.push("; AUTO GENERATED BY CADENCE SKILL PIXEL ARRAY LAYOUT GENERATOR");
  builder.push(`; Generated At: ${new Date().toISOString()}`);
  builder.push("; Author: Yongkai Zhang");
  builder.push(`; Copyright (c) ${new Date().getFullYear()} Yongkai Zhang`);
  builder.push("; ===================================================================");
  builder.push("");
  builder.push("procedure(createPixelArray()");
  builder.push("  let((");
  builder.push("    cv");
  builder.push("    master");
  builder.push("    inst");
  builder.push("    allInsts");
  builder.push("    dx");
  builder.push("    dy");
  builder.push("    currentY");
  builder.push("    maxActiveInst");
  builder.push("    cx");
  builder.push("    cy");
  builder.push("    bBox");
  builder.push("  )");
  builder.push("");
  builder.push("    printf(\"\\n===============================\\n\")");
  builder.push("    printf(\"Pixel Array Generation Start\\n\")");
  builder.push("    printf(\"===============================\\n\")");
  builder.push("");
  builder.push("    ; Ensure target library exists");
  builder.push("    unless(ddGetObj(");
  builder.push(`      "${config.top_lib}"`);
  builder.push("    )");
  builder.push("      error(");
  builder.push(`        "Library ${config.top_lib} does not exist!\\n"`);
  builder.push("      )");
  builder.push("    )");
  builder.push("");
  builder.push("    ; Check if cell layout exists. If so open it, otherwise create it.");
  builder.push("    if(ddGetObj(");
  builder.push(`      "${config.top_lib}"`);
  builder.push(`      "${config.top_cell}"`);
  builder.push("      \"layout\"");
  builder.push("    ) then");
  builder.push("      printf(");
  builder.push(`        "Cellview ${config.top_cell} layout already exists. Opening...\\n"`);
  builder.push("      )");
  builder.push("      cv = dbOpenCellViewByType(");
  builder.push(`        "${config.top_lib}"`);
  builder.push(`        "${config.top_cell}"`);
  builder.push("        \"layout\"");
  builder.push("        \"maskLayout\"");
  builder.push("        \"a\"");
  builder.push("      )");
  builder.push("    else");
  builder.push("      printf(");
  builder.push(`        "Cellview ${config.top_cell} layout does not exist. Creating new...\\n"`);
  builder.push("      )");
  builder.push("      cv = dbOpenCellViewByType(");
  builder.push(`        "${config.top_lib}"`);
  builder.push(`        "${config.top_cell}"`);
  builder.push("        \"layout\"");
  builder.push("        \"maskLayout\"");
  builder.push("        \"w\"");
  builder.push("      )");
  builder.push("    )");
  builder.push("");
  builder.push("    when(cv == nil");
  builder.push("      error(\"Cannot create or open target layout\\n\")");
  builder.push("    )");
  builder.push("");
  builder.push("    unwindProtect(");
  builder.push("      {");
  builder.push("    ; Clear old instances and mosaics to prevent stacking when re-running");
  builder.push("    when(cv");
  builder.push("      foreach(inst cv~>instances");
  builder.push("        dbDeleteObject(inst)");
  builder.push("      )");
  builder.push("      foreach(mosaic cv~>mosaics");
  builder.push("        dbDeleteObject(mosaic)");
  builder.push("      )");
  builder.push("    )");
  builder.push("");
  builder.push("    currentY = 0.0");
  builder.push("    allInsts = nil");
  builder.push("    rovInst = nil");
  builder.push("");

  const maxActiveRow = findMaxActiveRow(config);
  generateMosaics(builder, config, maxActiveRow);
  generateCentering(builder, config);

  builder.push("      }");
  builder.push("      when(cv");
  builder.push("        dbClose(cv)");
  builder.push("      )");
  builder.push("    )");
  builder.push("    printf(\"\\nPixel Array Generation Complete!\\n\")");
  builder.push("  )");
  builder.push(")");
  builder.push("");
  builder.push("; Execute procedure");
  builder.push("createPixelArray()");

  return builder.build();
}
